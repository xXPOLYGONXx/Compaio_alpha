    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        a.alter = Float.valueOf(jTextField1.getText());
        a.ralter = Float.valueOf(jTextField2.getText());
        a.lalter = Float.valueOf(jTextField3.getText());
        a.inflation = Float.valueOf(jTextField4.getText());
        a.einkommenn = Float.valueOf(jTextField6.getText());
        a.einkommenb = (float) (a.einkommenn/0.6);
        double c = (1+(a.inflation*0.01));
        double d = (a.ralter-a.alter);
        a.Kapitalb =(float)(Math.pow(c,d)*(a.einkommenn*0.7*12)*(a.lalter-a.ralter));
        Altersvorsorge_Tool_UI j = new Altersvorsorge_Tool_UI(a);
      j.setVisible(true);
      this.setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

